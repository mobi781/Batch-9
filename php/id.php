<?php
    /**
     * Increment/Decrement Operators
     * https://phpdocs.com/php/increment-decrement-operators
     * 
     */
     define("BR","<BR />");
     $a=20;

     //Post Inc
     echo $a++.BR;

     //Pre Inc
     echo ++$a.BR;

     //Pre Dec
    echo --$a.BR;

     //Post Dec

     echo $a--.BR;

    
?>